// bundle-routes.mjs — generates routes-data.js and segments-data.js from zwift-data.
// Run once (or after npm update zwift-data): node bundle-routes.mjs

import { routes, segments } from 'zwift-data';
import { writeFileSync } from 'fs';

// zwift-data v3+ uses world objects; v2 uses string slugs. Normalise to slug.
function worldSlug(w) {
  if (!w) return 'unknown';
  if (typeof w === 'string') return w;
  return w.slug ?? w.id ?? String(w);
}

const cleaned = routes.map(r => ({
  name:           r.name            ?? 'Unknown Route',
  slug:           r.slug            ?? null,
  world:          worldSlug(r.world),
  distance:       r.distance        ?? 0,
  elevation:      r.elevation       ?? 0,
  eventOnly:      r.eventOnly       ?? false,
  sports:         r.sports          ?? [],
  segments:       Array.isArray(r.segments) ? r.segments : [],
  segmentsOnRoute: Array.isArray(r.segmentsOnRoute)
    ? r.segmentsOnRoute.map(s => ({
        from: s?.from ?? null,
        to: s?.to ?? null,
        segment: s?.segment ?? null,
      })).filter(s => s.segment)
    : [],
  zwiftInsiderUrl: r.zwiftInsiderUrl ?? null,
  whatsOnZwiftUrl: r.whatsOnZwiftUrl ?? null,
}));

const cleanedSegments = segments
  .filter(segment => segment?.type === 'climb' || segment?.type === 'sprint')
  .map(segment => ({
    name: segment.name ?? 'Unknown Segment',
    slug: segment.slug ?? null,
    type: segment.type ?? null,
    world: worldSlug(segment.world),
    distance: segment.distance ?? 0,
    elevation: segment.elevation ?? null,
    avgIncline: segment.avgIncline ?? null,
    climbType: segment.climbType ?? null,
    stravaSegmentUrl: segment.stravaSegmentUrl ?? null,
  }));

writeFileSync(
  'routes-data.js',
  `// Auto-generated by bundle-routes.mjs — do not edit manually.\nexport const routes = ${JSON.stringify(cleaned, null, 2)};\n`
);

writeFileSync(
  'segments-data.js',
  `// Auto-generated by bundle-routes.mjs — do not edit manually.\nexport const segments = ${JSON.stringify(cleanedSegments, null, 2)};\n`
);

console.log(`Wrote ${cleaned.length} routes to routes-data.js`);
console.log(`Wrote ${cleanedSegments.length} segments to segments-data.js`);
console.log('Route sample:', JSON.stringify(cleaned[0], null, 2));
console.log('Segment sample:', JSON.stringify(cleanedSegments[0], null, 2));
